; ---- INC A
    ORG $1A00
    BRACCU16 INC_A_16bit
    INC8ANDSETNZ ALO
    NEXT
INC_A_16bit:    
    INC16 ALO AHI
    COMPUTENZFLAGS ALO AHI
    NEXT

    
    ; ---- INC d
    ORG $E600
    FETCHADDRESS_d TMP2 TMP3
    BRACCU16 INC_d_16bit
    LOAD TMP2 TMP3 V0 TMP4
    INC8ANDSETNZ TMP4
    STORE TMP2 TMP3 V0 TMP4
    NEXT
INC_d_16bit:
    LOAD16 TMP2 TMP3 V0 TMP4 TMP5
    INC16 TMP4 TMP5
    COMPUTENZFLAGS TMP4 TMP5
    STORE16 TMP2 TMP3 V0 TMP4 TMP5
    NEXT
